package test;

public class StringDemo {
	public static void main(String[] args){
		// 定义一个空字符串
		String s1 = "";
		System.out.println("s1="+ s1);
		String s2 = "Hllo";
		System.out.println("s2="+ s2);
		String s3 = "A\u005d\u005fB";
		System.out.println("s3="+ s3);
		String s4 = "Hello     imooc!";
		System.out.println("s4="+ s4);
	}
}
